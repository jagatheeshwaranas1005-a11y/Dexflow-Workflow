-- 3. Stored Procedure for Dashboard Stats
CREATE OR REPLACE FUNCTION get_dashboard_stats(start_date text, end_date text)
RETURNS json
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_start_timestamp timestamptz;
  v_end_timestamp timestamptz;
  v_total_submissions int;
  v_submissions_today int;
  v_total_audits int;
  v_audits_today int;
  v_total_errors int;
  v_pending_appeals int;
  v_approved_appeals int;
  v_rejected_appeals int;
  v_pending_queries int;
  v_resolved_queries int;
  v_error_breakdown json;
  v_users_by_role json;
  v_history json;
  result json;
BEGIN
  -- Parse dates explicitly as AT TIME ZONE 'UTC'
  v_start_timestamp := start_date::timestamptz;
  v_end_timestamp := end_date::timestamptz;

  SELECT count(*) INTO v_total_submissions 
  FROM submissions 
  WHERE "submittedAt" >= v_start_timestamp AND "submittedAt" < v_end_timestamp;

  SELECT count(*) INTO v_submissions_today 
  FROM submissions 
  WHERE date("submittedAt" AT TIME ZONE 'UTC') = current_date;

  SELECT count(*) INTO v_total_audits 
  FROM audits 
  WHERE "auditedAt" >= v_start_timestamp AND "auditedAt" < v_end_timestamp;

  SELECT count(*) INTO v_audits_today 
  FROM audits 
  WHERE date("auditedAt" AT TIME ZONE 'UTC') = current_date;

  SELECT count(*) INTO v_total_errors 
  FROM audits 
  WHERE "errorCategory" != 'No Error' AND "auditedAt" >= v_start_timestamp AND "auditedAt" < v_end_timestamp;

  SELECT count(*) INTO v_pending_appeals FROM appeals WHERE status = 'Pending' AND "appealedAt" >= v_start_timestamp AND "appealedAt" < v_end_timestamp;
  SELECT count(*) INTO v_approved_appeals FROM appeals WHERE status = 'Appeal Accepted' AND "appealedAt" >= v_start_timestamp AND "appealedAt" < v_end_timestamp;
  SELECT count(*) INTO v_rejected_appeals FROM appeals WHERE status = 'Appeal Rejected' AND "appealedAt" >= v_start_timestamp AND "appealedAt" < v_end_timestamp;
  
  SELECT count(*) INTO v_pending_queries FROM queries WHERE status = 'Pending' AND "queriedDate" >= v_start_timestamp AND "queriedDate" < v_end_timestamp;
  SELECT count(*) INTO v_resolved_queries FROM queries WHERE status = 'Resolved' AND "queriedDate" >= v_start_timestamp AND "queriedDate" < v_end_timestamp;

  SELECT json_object_agg("errorCategory", count) INTO v_error_breakdown 
  FROM (
    SELECT "errorCategory", count(*) as count 
    FROM audits 
    WHERE "errorCategory" != 'No Error' AND "auditedAt" >= v_start_timestamp AND "auditedAt" < v_end_timestamp 
    GROUP BY "errorCategory"
  ) t;

  SELECT json_object_agg(role, count) INTO v_users_by_role 
  FROM (
    SELECT role, count(*) as count 
    FROM users 
    WHERE "isActive" = 1 
    GROUP BY role
  ) t;

  SELECT json_agg(row_to_json(h)) INTO v_history
  FROM (
    WITH RECURSIVE dates AS (
       SELECT v_start_timestamp::date as date
       UNION ALL
       SELECT (date + interval '1 day')::date
       FROM dates
       WHERE date < (v_end_timestamp::date - interval '1 day')::date
    )
    SELECT 
      d.date,
      (SELECT count(*) FROM submissions s WHERE date(s."submittedAt" AT TIME ZONE 'UTC') = d.date) as submissions,
      (SELECT count(*) FROM audits a WHERE date(a."auditedAt" AT TIME ZONE 'UTC') = d.date) as audits,
      (SELECT count(*) FROM audits a WHERE date(a."auditedAt" AT TIME ZONE 'UTC') = d.date AND a."errorCategory" != 'No Error') as errors
    FROM dates d
  ) h;

  result = json_build_object(
    'totalSubmissions', coalesce(v_total_submissions, 0),
    'submissionsToday', coalesce(v_submissions_today, 0),
    'totalAudits', coalesce(v_total_audits, 0),
    'auditsToday', coalesce(v_audits_today, 0),
    'totalErrors', coalesce(v_total_errors, 0),
    'appeals', json_build_object('pending', coalesce(v_pending_appeals, 0), 'approved', coalesce(v_approved_appeals, 0), 'rejected', coalesce(v_rejected_appeals, 0)),
    'queries', json_build_object('pending', coalesce(v_pending_queries, 0), 'resolved', coalesce(v_resolved_queries, 0)),
    'errorBreakdown', coalesce(v_error_breakdown, '{}'::json),
    'usersByRole', coalesce(v_users_by_role, '{}'::json),
    'history', coalesce(v_history, '[]'::json)
  );

  RETURN result;
END;
$$;
