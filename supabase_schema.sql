-- 1. Create Tables

CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT,
  username TEXT UNIQUE,
  password TEXT,
  role TEXT,
  "empId" TEXT,
  process TEXT,
  designation TEXT,
  "isActive" SMALLINT DEFAULT 1
);

CREATE TABLE IF NOT EXISTS submissions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "artistName" TEXT,
  "empId" TEXT,
  "adId" TEXT,
  version TEXT,
  database TEXT,
  udac TEXT,
  status TEXT DEFAULT 'Submitted',
  "submittedAt" TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

CREATE TABLE IF NOT EXISTS queries (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  database TEXT,
  "adId" TEXT,
  version TEXT,
  "acquiredDate" DATE,
  udac TEXT,
  "queriedBy" TEXT,
  "queriedDate" TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
  "daysToExtract" INTEGER,
  "queryCode" TEXT,
  "queryCategory" TEXT,
  "queryDetails" TEXT,
  status TEXT DEFAULT 'Pending',
  validated TEXT DEFAULT 'Pending',
  raised TEXT DEFAULT 'Pending',
  remarks TEXT,
  "supervisorId" BIGINT,
  "approvedBy" TEXT,
  "resolvedAt" TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS audits (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "adId" TEXT,
  "artistName" TEXT,
  "prooferName" TEXT,
  "errorCategory" TEXT,
  "errorRemarks" TEXT,
  "checklistStatus" TEXT,
  "auditedAt" TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

CREATE TABLE IF NOT EXISTS appeals (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "errorId" BIGINT,
  "adId" TEXT,
  "artistName" TEXT,
  "appealDesc" TEXT,
  status TEXT DEFAULT 'Pending',
  "appealedAt" TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
  "resolutionNote" TEXT
);

CREATE TABLE IF NOT EXISTS config (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  type TEXT,
  value TEXT,
  UNIQUE(type, value)
);

